#Calculez le volume d’un parallélépipède dont la largeur, la longueur et la hauteur seront saisies au clavier.

print("=== CALCUL DU VOLUME D'UN PARALLELEPIPEDE ===")

largeur = int(input("Quelle est la largeur du parallélépipède ? "))
longueur = int(input("Quelle est la longueur du parallélépipède ? "))
hauteur = int(input("Quelle est la hauteur du parallélépipède ? "))

volume = largeur*longueur*hauteur

print(f"Le volume de ce parallélépipède est de {volume} cm³")

